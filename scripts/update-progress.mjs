/**
 * Regenerates the translation-progress table in README.md from sync-manifest.json.
 *
 * Called automatically by scripts/mark-translated.mjs after every translation,
 * and runnable standalone: node scripts/update-progress.mjs
 */

import {readFileSync, writeFileSync} from 'node:fs';
import {join, dirname} from 'node:path';
import {fileURLToPath} from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');
const MANIFEST_PATH = join(REPO_ROOT, 'sync-manifest.json');
const README_PATH = join(REPO_ROOT, 'README.md');

const START_MARKER = '<!-- TRANSLATION_PROGRESS:START (auto-generated by scripts/update-progress.mjs — do not edit by hand) -->';
const END_MARKER = '<!-- TRANSLATION_PROGRESS:END -->';

// Display order and Korean label for each top-level docs/ directory.
// Any top-level directory not listed here still appears (sorted after these),
// so a new docs/ section never silently drops out of the table.
const CHAPTER_LABELS = [
  ['(root)', '홈'],
  ['getting-started', '시작하기'],
  ['develop', '개발 및 빌드'],
  ['sql', 'SQL 다루기'],
  ['configure-and-operate', '설정 및 운영'],
  ['understand', '개념과 아키텍처'],
  ['api-reference', '클라이언트 및 API 참조'],
  ['tools', '도구'],
];

function escapeRegExp(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function chapterCounts(manifest) {
  const counts = new Map();
  for (const [path, meta] of Object.entries(manifest.files)) {
    const top = path.includes('/') ? path.split('/')[0] : '(root)';
    const c = counts.get(top) ?? {total: 0, translated: 0};
    c.total += 1;
    if (meta.status === 'translated') c.translated += 1;
    counts.set(top, c);
  }
  return counts;
}

/** Latest translatedAt among translated files, formatted "YYYY-MM-DD HH:mm UTC" (null if none yet). */
function lastUpdatedLabel(manifest) {
  let latest = null;
  for (const meta of Object.values(manifest.files)) {
    if (meta.status !== 'translated' || !meta.translatedAt) continue;
    if (!latest || meta.translatedAt > latest) latest = meta.translatedAt;
  }
  if (!latest) return '(아직 번역된 문서 없음)';
  return `${latest.slice(0, 16).replace('T', ' ')} UTC`;
}

function buildTable(counts) {
  const labeled = new Set(CHAPTER_LABELS.map(([key]) => key));
  const order = [
    ...CHAPTER_LABELS,
    ...[...counts.keys()].filter((key) => !labeled.has(key)).sort().map((key) => [key, key]),
  ];

  const rows = [];
  let totalAll = 0;
  let translatedAll = 0;
  for (const [key, label] of order) {
    const c = counts.get(key);
    if (!c) continue;
    totalAll += c.total;
    translatedAll += c.translated;
    const pct = Math.round((c.translated / c.total) * 100);
    rows.push(`| ${label} | ${c.translated} | ${c.total} | ${pct}% |`);
  }
  const totalPct = totalAll > 0 ? Math.round((translatedAll / totalAll) * 100) : 0;

  return [
    '| 영역 | 완료 | 전체 | 진행률 |',
    '| ---- | ---- | ---- | ------ |',
    ...rows,
    `| **전체** | **${translatedAll}** | **${totalAll}** | **${totalPct}%** |`,
  ].join('\n');
}

export function updateReadmeProgress() {
  const manifest = JSON.parse(readFileSync(MANIFEST_PATH, 'utf8'));
  const table = buildTable(chapterCounts(manifest));
  const lastUpdated = `마지막 갱신: ${lastUpdatedLabel(manifest)}`;
  const block = `${START_MARKER}\n${table}\n\n${lastUpdated}\n${END_MARKER}`;

  const readme = readFileSync(README_PATH, 'utf8');
  const blockPattern = new RegExp(`${escapeRegExp(START_MARKER)}[\\s\\S]*?${escapeRegExp(END_MARKER)}`);

  let next;
  if (blockPattern.test(readme)) {
    next = readme.replace(blockPattern, block);
  } else {
    const anchor = '## 로컬 개발';
    if (!readme.includes(anchor)) {
      throw new Error(`README.md: anchor "${anchor}" not found, cannot insert progress section`);
    }
    const section = `## 번역 진행 상태\n\n문서 영역(챕터)별 번역 완료 비율입니다. \`sync-manifest.json\`을 기준으로 자동 생성되며, 문서를 번역하고 \`node scripts/mark-translated.mjs\`를 실행할 때마다 갱신됩니다. 수동으로 다시 생성하려면 \`node scripts/update-progress.mjs\`를 실행하세요.\n\n${block}\n\n`;
    next = readme.replace(anchor, section + anchor);
  }

  if (next !== readme) {
    writeFileSync(README_PATH, next);
  }
  return next !== readme;
}

function isMain() {
  return import.meta.url === `file://${process.argv[1]}`;
}

if (isMain()) {
  const changed = updateReadmeProgress();
  console.log(changed ? 'README.md 번역 진행 상태를 갱신했습니다.' : 'README.md 번역 진행 상태가 이미 최신입니다.');
}
